export interface YearForecast {
  year: number;
  age: number;
  energy: number;
  arcanaName: string;
  short: string;
  theme: string;
  opportunities: string;
  risks: string;
  advice: string;
}

function yearEnergy(day: number, month: number, calendarYear: number): number {
  let y = 0;
  let cur = calendarYear;
  while (cur > 0) {
    y += cur % 10;
    cur = Math.floor(cur / 10);
  }
  return reduceNumber(reduceNumber(day) + month + reduceNumber(y));
}

