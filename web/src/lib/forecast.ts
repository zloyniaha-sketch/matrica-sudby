/** Прогноз на 10 лет от текущего календарного года */
export function buildTenYearForecast(
  day: number,
  month: number,
  birthYear: number,
  startYear?: number,
): YearForecast[] {
  const from = startYear ?? new Date().getFullYear();
  const result: YearForecast[] = [];

  for (let i = 0; i < 10; i++) {
    const year = from + i;
    const energy = yearEnergy(day, month, year);
    result.push({
      year,
      age: year - birthYear,
      energy,
      arcanaName: arcanaLabel(energy),
      short: arcanaShort(energy),
      theme: YEAR_THEMES[energy] ?? "Год проработки энергии аркана.",
      opportunities: YEAR_OPPORTUNITIES[energy] ?? "Новые возможности через проработку энергии года.",
      risks: YEAR_RISKS[energy] ?? "Избегай крайностей и автоматических реакций.",
      advice: YEAR_ADVICE[energy] ?? "Живи осознанно, опираясь на «плюс» аркана.",
    });
  }

  return result;
}
