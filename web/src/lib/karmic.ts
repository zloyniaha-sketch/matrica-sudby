export interface KarmicTailReport {
  d: number;
  m: number;
  n: number;
  dName: string;
  mName: string;
  nName: string;
  parts: { label: string; value: number; name: string; past: string; lesson: string; release: string }[];
  summary: string;
  tasks: string[];
  integration: string;
}

export function buildKarmicTailReport(d: number, m: number, n: number): KarmicTailReport {
  const values = [
    { label: "Прошлое (D)", value: d },
    { label: "Настоящее (M)", value: m },
    { label: "Будущее (N)", value: n },
  ];

  const parts = values.map(({ label, value }) => {
    const k = KARMIC_IN_TAIL[value] ?? KARMIC_IN_TAIL[1];
    return { label, value, name: arcanaLabel(value), ...k };
  });

  const fullD = ARCANA_FULL[d];
  const summary =
    `Кармический хвост ${d}–${m}–${n} (${arcanaLabel(d)}, ${arcanaLabel(m)}, ${arcanaLabel(n)}) — ` +
    `триада уроков души. Прошлое тянет сценарий «${parts[0].past.slice(0, 80)}…», ` +
    `настоящее просит «${parts[1].lesson.slice(0, 80)}…», будущее откроется, когда отпустишь «${parts[2].release.slice(0, 60)}…».`;

  const tasks = [
    `Проработать энергию ${d} (${arcanaLabel(d)}): ${fullD?.advice ?? "осознай паттерн и измени реакцию."}`,
    `В настоящем активировать ${m} (${arcanaLabel(m)}): ${KARMIC_IN_TAIL[m]?.lesson ?? ""}`,
    `Для будущего освободить ${n} (${arcanaLabel(n)}): ${KARMIC_IN_TAIL[n]?.release ?? ""}`,
  ];

  const integration =
    `Интеграция хвоста — не разовый инсайт, а ежедневная практика. ` +
    `Когда ловишь автоматическую реакцию из прошлого (D=${d}), останавливайся и выбирай ответ через энергию ${m}. ` +
    `Цель — выйти в линию ${n}, где урок считается пройденным.`;

  return {
    d,
    m,
    n,
    dName: arcanaLabel(d),
    mName: arcanaLabel(m),
    nName: arcanaLabel(n),
    parts,
    summary,
    tasks,
    integration,
  };
}
