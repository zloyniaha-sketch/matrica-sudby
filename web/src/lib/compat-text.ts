export function buildCompatSummary(
  scores: CompatibilityScores,
): { average: number; summary: string; strengths: string[]; challenges: string[] } {
  const values = [scores.sky, scores.earth, scores.relationship, scores.union, scores.harmony];
  const average = Math.round(values.reduce((a, b) => a + b, 0) / values.length);

  const sorted = COMPAT_DIMENSIONS.map((d) => ({
    ...d,
    score: scores[d.key],
    interp: interpretCompatScore(scores[d.key], d.key),
  })).sort((a, b) => b.score - a.score);

  const strengths = sorted.slice(0, 2).map((s) => `${s.label} (${s.score}%): ${s.interp.text}`);
  const challenges = sorted.slice(-2).reverse().map((s) => `${s.label} (${s.score}%): ${s.interp.tip}`);

  let summary: string;
  if (average >= 70) {
    summary = `Общая гармония пары — ${average}%. Сильная база для отношений: энергии дополняют друг друга, конфликты скорее точечные, чем системные.`;
  } else if (average >= 50) {
    summary = `Общая гармония — ${average}%. Пара имеет потенциал, но ключевые зоны требуют осознанной работы и диалога.`;
  } else {
    summary = `Общая гармония — ${average}%. Контраст энергий высок — отношения возможны, но потребуют терпения, границ и взаимного уважения к различиям.`;
  }

  return { average, summary, strengths, challenges };
}
