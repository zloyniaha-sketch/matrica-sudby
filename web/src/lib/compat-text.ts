type DimKey = keyof CompatibilityScores;
type Tier = "high" | "good" | "mid" | "low" | "hard";

function tier(score: number): Tier {
  if (score >= 80) return "high";
  if (score >= 60) return "good";
  if (score >= 40) return "mid";
  if (score >= 20) return "low";
  return "hard";
}

