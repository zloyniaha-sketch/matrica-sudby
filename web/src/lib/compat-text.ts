export function interpretCompatScore(
  score: number,
  dimension: DimKey = "harmony",
): CompatInterpretation {
  const t = tier(score);
  const meta = LEVEL[t];
  const content = DIM_TEXTS[dimension][t];
  return { ...meta, ...content };
}

export interface CompatDimension {
  key: DimKey;
  label: string;
  desc: string;
}

